class Calculadora
    def soma(primeiro_numero, segundo_numero)
        resulatado = primeiro_numero + segundo_numero  
        resulatado.round(1)  
    end

    def subtrai(primeiro_numero, segundo_numero)
        resulatado = primeiro_numero - segundo_numero  
        resulatado.round(1)     
    end

    def multiplica(primeiro_numero, segundo_numero)
        resulatado = primeiro_numero * segundo_numero  
        resulatado.round(1)     
    end

end